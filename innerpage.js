var data= JSON.parse(localStorage.getItem("inner_data"));
displayscreen(data);

function pricesorting(){
    var selected= document.querySelector("#sort").value;

    if(selected=="low"){
      data.sort(function(a,b){
          return Number(a.sold_price) - Number(b.sold_price);
      });  
    }
    if(selected=="high"){
        data.sort(function(a,b){
            return Number(b.sold_price) - Number(a.sold_price);
        });
    }
    console.log(data);
    displayscreen(data);
}

function skincategory(){
    var selected= document.querySelector("#skin-cat").value;
    var filterings = data.filter(function(elem){
        return elem.category==selected;
    })

    displayscreen(filterings);
}

function skinbrands(){
    var selected= document.querySelector("#skinbrand").value;

    var filtering = data.filter(function(elem){
        return elem.brand==selected;
    })

    displayscreen(filtering);
}


function displayscreen(data){
    document.querySelector("#product_col").textContent="";
    data.map(function(elem){
        var division= document.createElement("div");
        division.style.boxShadow= "rgba(0, 0, 0, 0.02) 0px 1px 3px 0px, rgba(27, 31, 35, 0.15) 0px 0px 0px 1px";
        division.style.borderRadius="8px";
        division.style.display="grid";
        division.style.gridTemplateRows="250px 180px"
        if(elem.name=="G.SERIES MEN’S CHAPPLE / SLIPPERS IN BLUE COLOUR"){
            division.addEventListener("click",function(){
                window.location.href="Venisua.html";
            })
        }
    
        
        var division2= document.createElement("div");
        division2.setAttribute("class","hover");
    
        var para4= document.createElement("p");
        para4.style.textAlign="center"
        para4.setAttribute("class","para4")

        var Image=document.createElement("img");
        Image.setAttribute("src",elem.Image);
        Image.style.width="100%"
        Image.setAttribute("class","image2")
    
        var division3= document.createElement("div");
      
    
        var para= document.createElement("p");
        para.textContent= elem.name;
        para.setAttribute("id","text");
        
    
        var para1= document.createElement("p")
        para1.textContent= elem.original_price;
        para1.style.color="grey";
    
        var para2= document.createElement("p");
        para2.textContent=elem.sold_price;
    
        para4.append(Image)
        division2.append(para4)
        division3.append(para,para1,para2);
        division.append(division2,division3)
        document.querySelector("#product_col").append(division);
    })
    
}

